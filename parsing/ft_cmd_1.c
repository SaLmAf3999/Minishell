#include "../minishell.h"

/*----------------------------------------------*/
void	to_last_cmd(t_cmd **cmd)
{
	while ((*cmd) && (*cmd)->next)
		(*cmd) = (*cmd)->next;
}
/*----------------------------------------------*/

/*----------------------------------------------*/
int	cmd_len(t_cmd *cmd)
{
	int	len;

	len = 0;
	if (!cmd)
		return (0);
	while (cmd)
	{
		len++;
		cmd = cmd->next;
	}
	return (len);
}
/*----------------------------------------------*/

/*----------------------------------------------*/
int	tab_len(t_token *token)
{
	int	len;

	len = 0;
	while (token && token->type != PIPE)
	{
		len++;
		token = token->next;
	}
	return (len);
}
/*----------------------------------------------*/

/*----------------------------------------------*/
char	*ft_make_str(char *str)
{
	char	*word;

	word = ft_strdup(str);
	return (word);
}
