#include "libft.h"

int	ft_tolower(int c)
{
	if (c <= 90 && c >= 65)
		c += 32;
	else
		return (c);
	return (c);
}

char	*ft_str_lower(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		str[i] = ft_tolower(str[i]);
		i++;
	}
	return (str);
}
